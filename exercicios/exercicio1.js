const numero1 = parseInt (prompt("Digite o primeiro numero"));
const numero2 = parseInt (prompt ("Digite o segundo numero"));

const modulo = numero1 % numero2;
console.log(`O módulo é ${modulo}`);

const soma = numero1 + numero2;
console.log(`A soma é ${soma}`);

const sub = numero1 - numero2;
console.log(`A subtração é ${sub}`);

const multiplicação = numero1 * numero2;
console.log(`A multiplicação é ${multiplicação}`);

const divisão = numero1 / numero2;
console.log(`A divisão é ${divisão}`);